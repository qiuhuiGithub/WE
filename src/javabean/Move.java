package javabean;

public class Move {
	private String ID;
	private String name;
	private String model;
	private String spec;
	private String from;
	private String to;
	private String moveman;
	private String recieveman;
	private String approval;
	private String movedate;
	private String shenpi;
	public String getID(){
		return ID;
	}
	public void setID(String ID){
		this.ID=ID;
	}
	public String getName(){
		return name;
	}
	public void setName(String name){
		this.name=name;
	}
	
	public String getModel(){
		return model;
	}
	public void setModel(String model){
		this.model=model;
	}
	
	public String getSpec(){
		return spec;
	}
	public void setSpec(String spec){
		this.spec=spec;
	}
	
	public String getFrom(){
		return from;
	}
	public void setFrom(String from){
		this.from=from;
	}
	public String getTo(){
		return to;
	}
	public void setTo(String to){
		this.to=to;
	}
	public String getMoveman(){
		return moveman;
	}
	public void setMoveman(String moveman){
		this.moveman=moveman;
	}
	public String getRecieveman(){
		return recieveman;
	}
	public void setRecieveman(String recieveman){
		this.recieveman=recieveman;
	}
	public String getApproval(){
		return approval;
	}
	public void setApproval(String approval){
		this.approval=approval;
	}
	public String getMovedate(){
		return movedate;
	}
	public void setMovedate(String movedate){
		this.movedate=movedate;
	}
	public String getShenpi(){
		return shenpi;
	}
	public void setShenpi(String shenpi){
		this.shenpi=shenpi;
	}
}
